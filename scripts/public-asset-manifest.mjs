const publicAssetsRoot = fileURLToPath(new URL("../src/http/public-assets/", import.meta.url));

export function contentHash(bytes) {
  return createHash("sha256").update(bytes).digest("hex").slice(0, 16);
}

export function createVersionedPublicAssetPath(logicalPath, bytes) {
  if (!logicalPath.startsWith("/public-assets/")) {
    throw new Error(`Public asset path must start with /public-assets/: ${logicalPath}`);
  }

  return `/public-assets/${contentHash(bytes)}/${logicalPath.slice("/public-assets/".length)}`;
}

export async function buildPublicAssetManifest({ sourceRoot = publicAssetsRoot } = {}) {
  const sourceBytes = new Map();

  for (const spec of publicAssetSpecs) {
    const bytes = await readFile(pathToFileURL(resolve(sourceRoot, spec.fileName)));
    sourceBytes.set(spec.logicalPath, spec.normalizeText ? Buffer.from(bytes.toString("utf8").replaceAll("\r\n", "\n"), "utf8") : bytes);
  }

  const outputBytes = new Map(sourceBytes);
  const versionedPaths = new Map();

  for (const spec of publicAssetSpecs.filter((asset) => !asset.transformReferences)) {
    versionedPaths.set(spec.logicalPath, createVersionedPublicAssetPath(spec.logicalPath, sourceBytes.get(spec.logicalPath)));
  }

  for (const spec of publicAssetSpecs.filter((asset) => asset.transformReferences)) {
    let transformed = sourceBytes.get(spec.logicalPath).toString("utf8");

    for (const [logicalPath, versionedPath] of versionedPaths) {
      transformed = transformed.replaceAll(logicalPath, versionedPath);
    }

    const bytes = Buffer.from(transformed, "utf8");
    outputBytes.set(spec.logicalPath, bytes);
    versionedPaths.set(spec.logicalPath, createVersionedPublicAssetPath(spec.logicalPath, bytes));
  }

  const definitions = publicAssetSpecs.map((spec) => {
    const versionedPath = versionedPaths.get(spec.logicalPath);
    const hash = versionedPath.split("/")[2];

    return {
      logicalPath: spec.logicalPath,
      versionedPath,
      fileName: spec.fileName,
      versionedFileName: `${hash}/${spec.fileName}`,
      contentType: spec.contentType,
      contentHash: hash,
    };
  });

  return {
    definitions,
    outputBytes,
    source: renderGeneratedManifest(definitions),
  };
}

export function renderGeneratedManifest(definitions) {
  return `/* This file is generated by scripts/generate-public-asset-manifest.mjs. */\n` +
    `/* Do not edit it directly. Change source assets or the generator instead. */\n\n` +
    `export const generatedPublicAssetDefinitions = ${JSON.stringify(definitions, null, 2)} as const;\n\n` +
    `const publicAssetUrlByLogicalPath = new Map<string, string>(\n` +
    `  generatedPublicAssetDefinitions.map((asset) => [asset.logicalPath, asset.versionedPath]),\n` +
    `);\n\n` +
    `export function publicAssetUrl(logicalPath: string): string {\n` +
    `  const versionedPath = publicAssetUrlByLogicalPath.get(logicalPath);\n\n` +
    `  if (!versionedPath) {\n` +
    `    throw new Error(\`Unknown public asset: \${logicalPath}\`);\n` +
    `  }\n\n` +
    `  return versionedPath;\n` +
    `}\n`;
}
